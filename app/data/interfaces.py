"""Protocol interfaces for the state store.

The full ``StateStoreProtocol`` is a composition of smaller domain protocols.
Modules SHOULD depend on the narrowest protocol they need (e.g.
``QueueRepository`` rather than the full ``StateStoreProtocol``).
"""

from __future__ import annotations

from typing import Dict, List, Optional, Protocol, runtime_checkable


# ═══════════════════════════════════════════════════════════════════════════════
# Domain repository protocols
# ═══════════════════════════════════════════════════════════════════════════════


@runtime_checkable
class QueueRepository(Protocol):
    """Reading queue operations."""

    def get_queue_item(self, paper_id) -> Optional[Dict]: ...
    def list_queue_items(self, status=None) -> List[Dict]: ...
    def upsert_queue_item(self, paper_id, status, source='', note='', tags=None, research_question_id=None, decision_context='') -> Dict: ...
    def mark_as_completed(self, paper_id, source='reading_page') -> Dict: ...


@runtime_checkable
class PaperRepository(Protocol):
    """Paper metadata and AI analysis storage."""

    def get_paper_metadata(self, paper_id) -> Optional[dict]: ...
    def save_paper_metadata(self, paper_id, metadata, source='', source_run_id='', first_seen_at=None, workspace_status='active') -> None: ...
    def get_paper_ai_analysis(self, paper_id) -> Optional[Dict]: ...
    def upsert_paper_ai_analysis(self, paper_id, analysis, model_name, prompt_version, status='ok', error_text='', evidence_claim_ids=None, confidence=None) -> Dict: ...
    def get_paper_embedding(self, paper_id) -> None: ...
    def save_paper_embedding(self, paper_id, embedding_bytes, model_name) -> None: ...
    def get_all_embeddings_for_model(self, model_name) -> None: ...


