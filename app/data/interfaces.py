@runtime_checkable
class StateStoreProtocol(
    QueueRepository,
    PaperRepository,
    ResearchQuestionRepository,
    EvidenceClaimRepository,
    CollectionRepository,
    SubscriptionRepository,
    AgentRepository,
    EntityRepository,
    EventRepository,
    JobRepository,
    RecommendationRepository,
    UserProfileRepository,
    SearchRepository,
    KeyValueRepository,
    Protocol,
):
    """Full state store contract — union of all domain repositories.

    Modules SHOULD depend on the narrowest domain protocol (e.g.
    ``QueueRepository``) when they don't need the full store.
    """
